# Arithmetic Operators:
# Python provides various arithmetic operators to perform mathematical calculations. Some common arithmetic operators include:
# - Addition (+): Adds two values together.
# - Subtraction (-): Subtracts one value from another.
# - Multiplication (*): Multiplies two values.
# - Division (/): Divides one value by another (returns a float).
# - Integer Division (//): Divides one value by another and returns the quotient as an integer.
# - Modulo (%): Returns the remainder after division.
# - Exponentiation (**): Raises a value to the power of another.