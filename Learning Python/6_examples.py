# Python Basics - Examples

# Data Types
# Integer
age = 25  # Represents a person's age

# Float
pi = 3.14  # Represents the value of pi

# String
name = "John Doe"  # Represents a person's name

# Boolean
is_active = True  # Represents whether a user is active or not

# Arithmetic Operators
x = 10
y = 3

# Addition
result = x + y  # Adds x and y together

# Subtraction
result = x - y  # Subtracts y from x

# Multiplication
result = x * y  # Multiplies x and y

# Division
result = x / y  # Divides x by y

# Integer Division
result = x // y  # Divides x by y and returns the integer quotient

# Modulo
result = x % y  # Returns the remainder of x divided by y

# Exponentiation
result = x ** y  # Raises x to the power of y

# String Programs
text1 = "Hello"
text2 = "World"

# Concatenation
result = text1 + " " + text2  # Concatenates text1 and text2 with a space in between

# Length
length = len(text1)  # Returns the length of text1

# Indexing
character = text1[0]  # Accesses the first character of text1

