# Data Types:
# In Python, there are several built-in data types that you can use to store different kinds of values. Some common data types include:
# - Integer: Represents whole numbers without any fractional part. For example: 1, 10, -5.
# - Float: Represents real numbers with a decimal point. For example: 3.14, -0.5, 2.0.
# - String: Represents a sequence of characters, such as text. They are enclosed in single ('') or double ("") quotes. For example: "Hello, World!", 'Python'.
# - Boolean: Represents either True or False.
# - List: Represents an ordered collection of values.
# - Tuple: Represents an ordered, immutable collection of values.
# - Dictionary: Represents a collection of key-value pairs.