# String Programs:
# Python offers powerful string manipulation capabilities. Here are a few common operations:
# - Concatenation: Combining two or more strings together using the + operator.
# - Length: Determining the length of a string using the len() function.
# - Indexing: Accessing individual characters within a string using square brackets [].
# - Slicing: Extracting a portion of a string using a specified range of indices.
# - Methods: Python provides numerous built-in string methods for tasks like finding substrings, replacing text, converting case, etc.